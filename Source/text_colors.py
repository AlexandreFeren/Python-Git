# Code taken from https://stackoverflow.com/questions/287871/how-do-i-print-colored-text-to-the-terminal
# Currently not sure how to set other colors and don't intend on using more than what is here
class b_colors:
    HEADER = '\033[95m'
    OK_BLUE = '\033[94m'
    OK_CYAN = '\033[96m'
    OK_GREEN = '\033[92m'
    WARNING = '\033[93m'
    FAIL = '\033[91m'
    END_C = '\033[0m'
    BOLD = '\033[1m'
    UNDERLINE = '\033[4m'
